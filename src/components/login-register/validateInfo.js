let check = (arr, values) => {
  const found = arr.some((el) => el.email === values.email);
  if (!found) arr.push(values);
  return arr;
};

let checkUserEmail = (arr, values) => {
  return arr.some(function (el) {
    return el.email === values.email;
  });
};

let checkUserPassword = (arr, values) => {
  return arr.some(function (el) {
    return el.password === values.password;
  });
};

